package IteratorExapmles;

public class Mobile {
	private String type;
	private double price;
	private String brand;
	public Mobile(String type,double d,String brand)
	{
	this.type=type;
	this.brand=brand;
	this.price=d;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	
	@Override
	public String toString()
	{
		return "["+"Type= "+type+","+"Price= "+price+","+"Brand= "+brand+"]";
//		return price+brand+type;
	}
	
}
